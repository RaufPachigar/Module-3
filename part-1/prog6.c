//6. Find area of Triangle Formula : A = 1/2 × b × h

#include<stdio.h>
int main()
{
	
	int l,b,area;
	printf("Enter a Breadth of triangle : \n");
	scanf("%d",&l);
	printf("Enter a height of triangle: \n");
	scanf("%d",&b);
	
	area=0.5*l*b;
	
	printf("Area of Triangle : %d\n",area);
	
	
}
